package com.xiaoe.common.entitys;

public class LearningRecord {

    private String lrId;
    private String lrType;
    private String lrTitle;
    private String lrImg;
    private String lrDesc;

    public String getLrId() {
        return lrId;
    }

    public String getLrType() {
        return lrType;
    }

    public String getLrTitle() {
        return lrTitle;
    }

    public String getLrImg() {
        return lrImg;
    }

    public String getLrDesc() {
        return lrDesc;
    }

    public void setLrId(String lrId) {
        this.lrId = lrId;
    }

    public void setLrType(String lrType) {
        this.lrType = lrType;
    }

    public void setLrTitle(String lrTitle) {
        this.lrTitle = lrTitle;
    }

    public void setLrImg(String lrImg) {
        this.lrImg = lrImg;
    }

    public void setLrDesc(String lrDesc) {
        this.lrDesc = lrDesc;
    }
}
